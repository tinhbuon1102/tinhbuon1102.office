<h1>ロゴ設定方法</h1>
</div>
<h4>1.画像をアップロード</h4>
<div class="help_body_content">
<p>設定ページにて、<strong>[ロゴ設定]</strong>にいき、<strong>[アップロード]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/10.jpg" /></div>
<p>アップロード画面が表示されますので、<strong>[ファイルを選択]</strong>ボタンをクリックし、画像をアップロードしてください。</p>
<div class="help-shot"><img src="/images/help/setting/11_share.jpg" /></div>
</div>
<h4>2.画像をトリミング</h4>
<div class="help_body_content">
<p>アップロード後、画像のトリミング調整ができますので、トリミングを行い、<strong>[メイン画像として設定]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/12_share.jpg" /></div>
</div>
<h4>3.保存</h4>
<div class="help_body_content">
<p>アップロード完了後、ページ内、右上にある<strong>[保存]</strong>ボタンをクリックし、ロゴ画像の追加が完了となります。</p>
<div class="help-shot"><img src="/images/help/setting/13_share.jpg" /></div>
</div>