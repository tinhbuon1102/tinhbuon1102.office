<h1>オファー方法</h1>
</div>
<h4>オファーとは？</h4>
<div class="help_body_content">
<p>オファーとはあなたが提供するスペースを使ってもらいたい<strong>気になる人材のユーザーにアプローチ</strong>をするアクションです。</p>
</div>
<h4>1.ユーザー検索ページを開く</h4>
<div class="help_body_content">
<p>メニューから<strong>[利用者を探す]</strong>をクリックしてください。<br/>
ユーザーリストが表示されますので、オファーを送りたいユーザーの<strong>[オファーする]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/offer/1.jpg" /></div>
</div>
<h4>2.オファーを送る</h4>
<div class="help_body_content">
<p>あなたの管理しているスペースが表示されますので、オファーをしたいスペースにチェックし、<strong>[オファーを送る]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/offer/2.jpg" /></div>
</div>
<h4>3.オファー履歴を確認</h4>
<div class="help_body_content">
<p>あなたがオファーしたユーザーは<strong>[オファーリスト]</strong>ページで確認ができます。</p>
<div class="help-shot"><img src="/images/help/offer/3.jpg" /></div>
</div>




