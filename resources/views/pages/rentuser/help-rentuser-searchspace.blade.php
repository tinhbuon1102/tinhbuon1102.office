<h4>スペース検索ページに推移</h4>
<div class="help_body_content">
<p>メニューから<strong>[スペースを探す]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/search/1.jpg" /></div>
</div>
<h4>2.検索条件を指定し検索</h4>
<div class="help_body_content">
<p>以下の項目にて検索条件が指定できます。ご希望の条件を選択肢、検索してください。</p>
<ol class="list-type-alp">
<li><strong>[地域を選択]</strong><br/>都道府県、市区町村から検索できます。</li>
<li><strong>[利用タイプ]</strong><br/>時間毎・日毎・週毎・月毎から検索できます。</li>
<li><strong>[日にち・時間指定]</strong><br/>日にち、時間指定で予約可能なスペースを検索できます。</li>
<li><strong>[詳細検索]</strong><br/>より詳しい条件にて検索できます。</li>
</ol>
<div class="help-shot"><img src="/images/help/search/2.jpg" /></div>
<p>以下の項目にて詳細検索ができます。</p>
<ol class="list-type-alp">
<li><strong>[スペースタイプ]</strong><br/>各スペースタイプから検索できます。(複数選択可)</li>
<li><strong>[利用人数]</strong><br/>利用人数から検索できます。</li>
<li><strong>[希望利用料金]</strong><br/>利用金額から検索できます。</li>
<li><strong>[利用時間帯]</strong><br/>利用可能時間帯から検索できます。</li>
<li><strong>[その他設備]</strong><br/>スペース利用に含まれている設備から検索できます。(複数選択可)</li>
</ol>
