<div class="help_header">
<h1>スペース編集方法</h1>
</div>
<h4>1.スペースページ編集画面を開く</h4>
<div class="help_body_content">
<p>ログイン後、メニューから<strong>[提供中スペース]</strong>をクリックし、スペースページに推移してください。<br/>
スペースページに移動しましたら、登録済みのスペース一覧が表示されます。<br/>
リスト右側に[<span class="font-icon fonticon-dots-three-horizontal"></span>]がありますので、そちらをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/10.jpg" /></div>
<p>クリックしますと、オプションメニューが表示されますので、そちらから<strong>[編集]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/11.jpg" /></div>
</div>
<a id="spaceinfo"></a>
<h4>2.スペース情報を編集</h4>
<div class="help_body_content">
<p>変更したい、スペース情報の各項目を編集してください。</p>
<p><i class="fa fa-info-circle" aria-hidden="true"></i>スペース情報入力のガイドラインは<a href="{{url('help/shareuser/addspace#spaceinfo')}}"><strong>[こちら]</strong></a>からご参照ください。</p>
<div class="help-shot"><img src="/images/help/list-space/12.jpg" /></div>
</div>

<h4>3.保存してスペース更新完了</h4>
<div class="help_body_content">
<p>全ての情報が入力し終えましたら、右上の<strong>[保存]</strong>ボタンをクリックし、更新が完了となります。</p>
</div>
