<h1>お気に入り追加方法</h1>
</div>
<h4>1.お気に入りに追加</h4>
<div class="help_body_content">
<p>スペース詳細ページにて<strong>[お気に入り]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/fav/1.jpg" /></div>
<p>お気に入りに追加されたら、ボタンがオレンジになります。</p>
<div class="help-shot"><img src="/images/help/fav/2.jpg" /></div>
</div>
<h4>2.お気に入りリストを確認</h4>
<div class="help_body_content">
<p>マイページで左メニューの<strong>[お気に入り]</strong>をクリックしてください。<br/>
追加されたお気に入りのスペースが表示されます。</p>
<div class="help-shot"><img src="/images/help/fav/3.jpg" /></div>
<p>お気に入りにを外したい場合は、<strong>[お気に入り追加済]</strong>ボタンにカーソルをあてると、<strong>[お気に入り取り消し]</strong>メニューがでてくるので、それをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/fav/4.jpg" /></div>
</div>




