<div class="help_header">
<h1>実績追加方法</h1>
</div>
<h4>1.プロフィール編集ページを開く</h4>
<div class="help_body_content">
<p>ログイン後、ダッシュボードの右側にある<strong>[プロフィールアイコン]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/profile/1.jpg" /></div>
</div>
<a id="howpf"></a>
<h4>2.編集画面を表示</h4>
<div class="help_body_content">
<p>右側にある<strong>[プロフィール編集]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/profile/2.jpg" /></div>
<p>下にスクロールすると<strong>[+実績を追加]</strong>ボタンがあるので、クリックしてください。</p>
<div class="help-shot"><img src="/images/help/profile/p1.jpg" /></div>
</div>
<h4>3.実績内容を編集</h4>
<div class="help_body_content">
<p>以下の手順で編集・追加ができます。</p>
<ol class="list-type-num">
<li><strong>写真を追加</strong><br/>ファイルを選択からアップロードしてください。</li>
<li><strong>タイトル</strong><br/>実績のタイトルを入力してください。</li>
<li><strong>説明</strong><br/>実績の説明を入力してください。</li>
<li>1~3の項目を設定し終えたら、<strong>[実績を追加]</strong>ボタンをクリックし、追加完了です。</li>
</ol>
