<h4>2.予約詳細を確認</h4>
<div class="help_body_content">
<p>以下の項目の予約詳細が確認できます。確認をしてから、次へ進んで下さい。</p>
<ol class="list-type-alp">
<li>利用予約日・時間を変更する場合は、再度選択してください。</li>
<li>利用人数を選択してください。</li>
<li>利用金額の単価、利用タイプ、利用時間を確認してください。</li>
<li>利用合計金額と内訳を確認してください。 (→<a href="#" target="_blank" class=""><strong>手数料についてはこちら</strong></a>)</li>
<li>備考がある場合は、記入してください。</li>
<li>借主利用規約を<strong>必ずご確認の上、同意する場合は同意するにチェック</strong>をしてください。<br/>※チェックを入れないと次へ進めません。</li>
</ol>
<div class="help-shot"><img src="/images/help/book/5.jpg" /></div>
</div>

