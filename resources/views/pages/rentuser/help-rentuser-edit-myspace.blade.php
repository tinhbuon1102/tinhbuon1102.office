<h1>スペース希望条件設定</h1>
</div>
<h4>1.希望スペース設定ページを開く</h4>
<div class="help_body_content">
<p>マイページから、左メニューにある<strong>[希望スペース設定]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/profile/myspace/1.jpg" /></div>
</div>
<a id="setmyspace"></a>
<h4>2.スペースの希望条件を設定する</h4>
<div class="help_body_content">
<p>スペース希望条件の設定では以下の項目が設定できます。</p>
<ol class="list-type-alp myspace-type">
<li><strong>スペースタイプ</strong><br/>
<ul class="use-type space-type-list">
<li>
<span class="icon-left"><i class="iconbutton-icon workspace-type-icon-Desk"></i></span>
<span class="disc-right">
<strong>デスク</strong><br/>コワーキングスペースや、個別デスクなどのスペース<br/>分類されるスペースタイプ：コワーキング/オープンデスク/専用デスク
</span>
</li>
<li>
<span class="icon-left"><i class="iconbutton-icon workspace-type-icon-Meeting"></i></span>
<span class="disc-right">
<strong>会議室</strong><br/>会議室タイプのスペース<br/>分類されるスペースタイプ：会議室
</span>
</li>
<li>
<span class="icon-left"><i class="iconbutton-icon workspace-type-icon-Office"></i></span>
<span class="disc-right">
<strong>オフィス</strong><br/>1人用の個室オフィス、複数人用の個室やフロア全体を利用できるオフィススペース<br/>分類されるスペースタイプ：プライベートオフィス/チームオフィス/オフィス
</span>
</li>
<li>
<span class="icon-left"><i class="iconbutton-icon workspace-type-icon-Training"></i></span>
<span class="disc-right">
<strong>セミナースペース</strong><br/>セミナー用のスペース<br/>分類されるスペースタイプ：セミナースペース
</span>
</li>
<li>
</ul>
</li>
<li><strong>希望地域</strong><br/>探しているスペースの地域を選択してください。</li>
<li><strong>希望利用料金</strong><br/>1時間/1日/1週間/1ヶ月あたりのどれかを選択し、希望利用料金を選択してください。</li>
<li><strong>利用時間帯</strong><br/>スペースの利用可能時間帯の希望時間帯を選択してください。</li>
<li><strong>スペース面積</strong><br/>スペースの希望面積を選択してください。</li>
<li><strong>利用人数</strong><br/>スペースの利用希望人数を選択してください。</li>
<li><strong>どんな事業に興味がありますか？</strong><br/>どんな事業のワークスペースに興味があるか選択してください。</li>
<li><strong>備考</strong><br/>その他、スペースに関しての希望があれば記述してください。</li>
<li><strong>スペースに含まれる設備</strong><br/>スペースにある備品の数を希望があれば入力してください。</li>
<li><strong>その他設備</strong><br/>スペースの希望設備を選択してください。</li>
<li><strong>ビル設備</strong><br/>スペースがあるビルの希望設備を選択してください。</li>
</ol>
<div class="help-shot"><img src="/images/help/profile/myspace/2.jpg" /></div>
</div>
<h4>3.保存する</h4>
<div class="help_body_content">
<p>右上にある<strong>[保存]</strong>ボタンをクリックし、設定が完了です。</p>
<div class="help-shot"><img src="/images/help/profile/myspace/3.jpg" /></div>
</div>