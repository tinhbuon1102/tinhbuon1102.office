<h1>レント会員登録の手順</h1>
</div>
<h4>1.メールアドレス・ユーザー名・パスワードを入力</h4>
<div class="help_body_content">
<div class="help-shot"><img src="/images/help/register/01.jpg" /></div>
<p>トップページにある"<strong>会員登録</strong>"をクリックしてください。<br/>
入力フォームが表示されるので、メールアドレス、ユーザー名、パスワードを入力し、"<strong>利用する</strong>"を選び、"アカウント作成"をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/register/02_rent.jpg" /></div>
</div>
<h4>2.基本情報を入力</h4>
<div class="help-shot"><img src="/images/help/register/03_rent.jpg" /></div>
<div class="help_body_content">
アカウントの基本情報をご入力ください。<br/>
入力後、"登録する"をクリックしてください。
</div>
<h4>3.仮登録完了</h4>
<div class="help-shot"><img src="/images/help/register/04.jpg" /></div>
<div class="help_body_content">
確認のため、仮登録完了メールが送られますので、ご登録されたメールアドレス宛のメールをご確認下さい。
</div>
<h4>4.メールアドレス認証・本登録完了</h4>
<div class="help-shot"><img src="/images/help/register/05_rent.jpg" /></div>
<div class="help_body_content">
仮登録完了メールに、メールアドレス認証のためのリンクがありますので、そちらをクリックし、メールアドレス認証を完了させてください。<br/>
メールアドレス認証が完了すると、登録が完了となり、ログインアクセスが可能となります。
</div>
