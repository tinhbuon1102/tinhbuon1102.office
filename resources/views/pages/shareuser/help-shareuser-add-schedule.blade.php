<h1>スケジュール追加方法</h1>
</div>
<h4>1.カレンダーページを開く</h4>
<div class="help_body_content">
<p>ログイン後、メニューから<strong>[マイページ]</strong>をクリックし、左メニューから<strong>[カレンダー]</strong>をクリックしてください。<br/>
またはスケジュールが設定されていない場合、アラートが表示されますので、そちらから<strong>[予約可能日を設定]</strong>クリックしてください。</p>
<div class="help-shot"><img src="/images/help/schedule/1.jpg" /></div>
</div>
<a id="spaceinfo"></a>
<h4>2.予約可能日を設定</h4>
<div class="help_body_content">
<p>予約可能日として受付可能な時間/日/週/月をカレンダーにてクリックしてください。</p>

<div class="mgb-30">
<h5 id="acMenu">時間毎タイプの設定方法</h5>
<div class="ac-content">
<p>予約受付をする時間帯をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/schedule/10.jpg" /></div>
<p>クリックすると、スケジュール設定の詳細が画面が表示されます。</p>
<ol class="list-type-alp">
<li><strong>利用開始時間</strong><br/>その日付で利用可能な時間帯の開始時間を選択してください。</li>
<li><strong>利用終了時間</strong><br/>その日付で利用可能な時間帯の終了時間を選択してください。</li>
<li><strong>この予約を繰り返します</strong><br/>利用可能な時間帯を一括で複数の日程にも設定したい場合、こちらにチェックを入れてください。</li>
</ol>
<p>その日付のみで予約受付を設定したい場合は、AとBを設定後、<strong>[保存]</strong>をクリックして下さい。</p>
<div class="help-shot"><img src="/images/help/schedule/11.jpg" /></div>
<p><strong>[この予約を繰り返します]</strong>にチェックを入れた場合、以下の様にオプションが表示されます。</p>
<ol class="list-type-alp">
<li><strong>繰り返し期間</strong><br/>いつからいつまでの期間で同じ予約設定を繰り返したいか、日付を選択してください。</li>
<li><strong>曜日の除外設定</strong><br/>特定の曜日を除外したい場合は、チェックを外して下さい。</li>
</ol>
<div class="help-shot"><img src="/images/help/schedule/12.jpg" /></div>
<p>保存をクリックすると以下の様に予約受付時間が保存されます。</p>
<div class="help-shot"><img src="/images/help/schedule/13.jpg" /></div>
</div>
</div>

