<h1>売上確認方法</h1>
</div>
<h4>支払通知書とは</h4>
<div class="help_body_content">
<p>支払通知書とはスペース提供料金の売上として、hOur Officeからあなたに支払われる金額が確認できる通知書となります。</p>
</div>
<h4>1.支払い通知書ページを開く</h4>
<div class="help_body_content">
<p>ログイン後、メニューから<strong>[マイページ]</strong>をクリックし、左メニューから<strong>[支払い通知書]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/sales/1.jpg" /></div>
</div>
<h4>2.支払通知書一覧の見方</h4>
<div class="help_body_content">
<p>支払通知書一覧では、以下のように売上の概要が確認できます。<br/>より詳細を確認するためには<strong>[詳細]</strong>ボタンをクリックしてください。</p>
<ol class="list-type-alp">
<li><strong>予約番号</strong><br/>支払される売上がどの予約のものか確認できる予約番号です。</li>
<li><strong>金額</strong><br/>支払われる金額です。</li>
<li><strong>ステータス</strong><br/>予約の支払ステータスです。</li>
</ol>
<div class="help-shot"><img src="/images/help/sales/2.jpg" /></div>
</div>
<h4>3.支払通知書詳細の見方</h4>
<div class="help_body_content">
<p>支払通知書詳細では、以下のように売上の詳細が確認できます。</p>
<ol class="list-type-alp row">
<li class="col-md-6"><strong>日付</strong><br/>売上が確定され、支払通知書が作成された日付です。</li>
<li class="col-md-6"><strong>支払番号</strong><br/>支払通知書の支払番号です。</li>
<li class="col-md-6 clear"><strong>合計金額</strong><br/>この予約分の支払われる合計金額です。</li>
<li class="col-md-6"><strong>売上内訳</strong><br/>この予約分の売上内訳です。</li>
<li class="col-md-6 clear"><strong>手数料</strong><br/>売上から引かれる手数料(税込売上分の10%)の金額です。</li>
<li class="col-md-6"><strong>合計金額</strong><br/>この予約分の支払われる合計金額です。</li>
<li class="col-md-6 clear"><strong>利用情報</strong><br/>この売上の利用情報です。</li>
<li class="col-md-6"><strong>支払情報</strong><br/>この売上の予約支払情報です。</li>
</ol>
