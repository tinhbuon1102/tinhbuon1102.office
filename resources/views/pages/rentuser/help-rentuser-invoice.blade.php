<div class="help_header">
<h1>支払履歴確認方法</h1>
</div>
<h4>1.請求書ページを開く</h4>
<div class="help_body_content">
<p>マイページで左メニューの<strong>[請求書]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/invoice/1.jpg" /></div>
<p>請求書一覧ページが表示されますので、詳細を見るには<strong>[詳細]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/invoice/2.jpg" /></div>
</div>
<h4>2.支払詳細をみる</h4>
<div class="help_body_content">
<p>請求書詳細では以下の項目が確認できます。</p>
<ol class="list-type-alp row">
<li class="col-md-6"><strong>日付</strong><br/>決済がされ、請求書が作成された日付です。</li>
<li class="col-md-6"><strong>支払い番号</strong><br/>請求書の支払番号です。</li>
<li class="col-md-6"><strong>合計金額</strong><br/>この予約分の支払われた合計金額です。</li>
<li class="col-md-6"><strong>支払金額内訳</strong><br/>この予約分の支払金額内訳です。</li>
<li class="col-md-6"><strong>手数料</strong><br/>予約時の手数料の金額です。</li>
<li class="col-md-6"><strong>合計金額</strong><br/>この予約分の支払われた合計金額です。</li>
<li class="col-md-6"><strong>利用情報</strong><br/>この支払の予約利用情報です。</li>
<li class="col-md-6"><strong>支払情報</strong><br/>この予約の支払情報です。</li>
</ol>
