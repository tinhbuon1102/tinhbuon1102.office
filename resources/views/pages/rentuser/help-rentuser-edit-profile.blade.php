                        <li>写真のトリミングをしてください。<p class="alignCenter"><img src="/images/help/profile/pic2.jpg" /></p></li>
                        <li>[メイン画像として設定]をクリックし、保存してください。<p class="alignCenter"><img src="/images/help/profile/pic3.jpg" /></p></li>
                    </ol>
                    <!-- /#modalContents --></div>
                <!-- /#caution01 --></div>
                </div><!--/modals-->
</li>
