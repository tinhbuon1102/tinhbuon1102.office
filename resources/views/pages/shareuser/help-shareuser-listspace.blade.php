<div class="help_header">
<h1>スペース掲載方法</h1>
</div>
<h4>1.スペースページを開く</h4>
<div class="help_body_content">
<p>ログイン後、ダッシュボードページから<strong>[スペース登録]</strong>アラート、またはあなたへのお知らせ欄に表示されている<strong>[スペースを追加]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/1.jpg" /></div>
<p>または、メニューから<strong>[提供中スペース]</strong>をクリックし、<strong>[スペースを追加]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/2_2.jpg" /></div>
</div>
<h4>2.スペース情報を入力してスペースを登録</h4>
<div class="help_body_content">
<p>スペース情報を入力してください。入力後、右上の<strong>[保存]</strong>ボタンをクリックしてください。</p>
<p><i class="fa fa-info-circle" aria-hidden="true"></i>スペース情報入力のガイドラインは<a href="{{url('help/shareuser/addspace#spaceinfo')}}"><strong>[こちら]</strong></a>からご参照ください。</p>
<div class="help-shot"><img src="/images/help/list-space/9.jpg" /></div>
</div>
<h4>3.予約受付日を設定</h4>
<div class="help_body_content">
<p>カレンダーページを開き、スペース利用可能なスケジュールを設定してください。</p>
<p><i class="fa fa-info-circle" aria-hidden="true"></i>スケジュール追加方法のガイドラインは<a href="{{url('help/shareuser/add-schedule')}}"><strong>[こちら]</strong></a>からご参照ください。</p>
<div class="help-shot"><img src="/images/help/list-space/13.jpg" /></div>
</div>
<h4>4.サイトに掲載完了</h4>
<div class="help_body_content">
<p>以上であなたのスペースがサイトに掲載されます。</p>
<div class="help-shot"><img src="/images/help/list-space/14.jpg" /></div>
</div>