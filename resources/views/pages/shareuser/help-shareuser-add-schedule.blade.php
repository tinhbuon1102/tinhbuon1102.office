<div class="help-shot"><img src="/images/help/schedule/7.jpg" /></div>
<p><strong>[この予約を繰り返します]</strong>にチェックを入れた場合、以下の様にオプションが表示されます。<br/>同じ予約受付設定をしたい+何週分かの数字を入力してください。<br/>
例)1と入力すると、2週間分の予約受付が保存されます。</p>
<div class="help-shot"><img src="/images/help/schedule/8.jpg" /></div>
