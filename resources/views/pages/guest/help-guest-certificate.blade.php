<h1>会員審査について</h1>
</div>
<div class="help_body_content">
<p>オフィスポではアカウント作成、スペース掲載の不正を防ぐため、会員登録後、<strong>[本人確認手続き]</strong>にて審査を行い、サイトサービスを利用できるアカウント権限を与えています。</p>
<h5>本人確認書類について</h5>
<p>本人確認には以下の書類のいずれかが必要です。
事前に必ず注意事項をご確認いただき、必要な箇所をスキャンして、GIF, JPG, PNG, PDF のいずれかの形式でご用意ください。</p>
<p><strong>&lt; 必要書類 &gt;</strong></p>
