<h1>パスワード変更方法</h1>
</div>
<h4>1.現在のパスワードと新しいパスワードを入力</h4>
<div class="help_body_content">
<p>設定ページにて、パスワード項目にいき、現在のパスワードと新しいパスワードを入力してください。<br/>入力後、<strong>[パスワード変更]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/08.jpg" /></div>
<p>パスワード変更完了メッセージが表示されましたら、パスワード変更完了となります。</p>
