<h4>1.口座情報を編集・保存</h4>
<div class="help_body_content">
<p>設定ページにて、<strong>[支払受取情報]</strong>にいき、<strong>[編集]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/14_share.jpg" /></div>
<p>クリックすると、振込口座情報を入力するフォームが表示されます。各項目を入力し、<strong>[保存]</strong>ボタンをクリックすると設定完了です。</p>
<div class="help-shot"><img src="/images/help/setting/15_share.jpg" /></div>
</div>