<h1>スケジュール確認方法</h1>
</div>
<h4>1.カレンダーページを開く</h4>
<div class="help_body_content">
<p>ログイン後、メニューから<strong>[マイページ]</strong>をクリックし、左メニューから<strong>[カレンダー]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/schedule/19.jpg" /></div>
</div>
<a id="spaceinfo"></a>
<h4>2.スケジュールを確認</h4>
<div class="help_body_content">
<ol class="list-type-alp">
<li><p><strong>確認したいスペースの利用タイプを選択</strong><br/>
スペースの利用タイプから時間毎・日毎・週毎・月毎で、スペースカレンダーは振り分けられています。属するタイプを選択してください。</p></li>
<li><p><strong>確認したいスペース名を選択</strong><br/>
左タブから確認したいスペース名を選択選択してください。</p></li>
<li><p><strong>空きスケジュールを確認</strong><br/>
青いラベルが空いているスケジュールです。</p></li>
<li><p><strong>予約済みスケジュールを確認</strong><br/>
紫のラベルが空いているスケジュールです。</p></li>
</ol>
<div class="help-shot"><img src="/images/help/schedule/20.jpg" /></div>
<p>予約済みのスケジュールをマウスオーバーすると、予約概要を確認できます。</p>
<div class="help-shot"><img src="/images/help/schedule/21.jpg" /></div>
</div>

