<h1>支払い方法の登録方法</h1>
</div>
<h4>1.設定ページを開く</h4>
<div class="help_body_content">
<p>ダッシュボードページ、右側から<strong>[編集]</strong>、またはマイページ左メニューから<strong>[設定]</strong>リンクをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/01_rent.jpg" /></div>
<div class="help-shot"><img src="/images/help/setting/01_2_rent.jpg" /></div>
</div>
<h4>2.支払い方法を追加</h4>
<div class="help_body_content">
<p>設定ページにて、決済方法の欄にて<strong>[支払い方法を追加]</strong>ボタンをクリックして下さい。</p>
<div class="help-shot"><img src="/images/help/setting/addp1.jpg" /></div>
<p><strong>[クレジットカード]</strong>か<strong>[Paypal]</strong>を選択し、下記の通り追加を行ってて下さい。</p>
<div class="mgb-20">
<h5 id="acMenu">クレジットカードの場合</h5>
<div class="ac-content">
<p>カード情報を入力し、クレジットカード追加をクリック</p>
<div class="help-shot"><img src="/images/help/setting/addp2.jpg" /></div>
<p>カード情報の追加が成功しましたら、以下の様に表示されます。</p>
<div class="help-shot"><img src="/images/help/setting/addp4.jpg" /></div>
</div>
</div>
<div class="mgb-20">
<h5 id="acMenu">Paypalの場合</h5>
<div class="ac-content">
<p>Paypalボタンをクリック</p>
<div class="help-shot"><img src="/images/help/setting/addp3.jpg" /></div>
<p>Paypalのサイトへ推移します。Paypalのアカウントをお持ちの方はログイン、そうでない方はPaypalアカウントを作成し、Paypalサイトの手順にて進めて下さい。</p>
<div class="help-shot"><img src="/images/help/setting/addp5.jpg" /></div>
<p>設定が完了すると、オフィスポページへ戻り、以下の様に表示されます。</p>
<div class="help-shot"><img src="/images/help/setting/addp6.jpg" /></div>
</div>
</div>
</div>
