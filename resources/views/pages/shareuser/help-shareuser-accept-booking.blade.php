<div class="help_header">
<h1>予約申込みの受付方法</h1>
</div>
<h4>1.予約一覧ページを開く</h4>
<div class="help_body_content">
<p>ログイン後、メニューから<strong>[予約リスト]</strong>をクリック、または左メニューから<strong>[予約]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/booking/share/1.jpg" /></div>
</div>
<h4>2.受付を行う予約詳細を確認する</h4>
<div class="help_body_content">
<p>予約受付の処理を行いたい予約の<strong>[詳細]</strong>ボタンをクリックし、予約申し込み内容をを確認してください。</p>
<div class="help-shot"><img src="/images/help/booking/share/5.jpg" /></div>
</div>
<h4>3.予約受付処理を行う</h4>
<div class="help_body_content">
<h5>支払受取を行う(予約受付処理)</h5>
<p>予約を受け付ける場合は、以下の操作方法で支払受取の処理をして下さい。</p>
<p><strong>[支払情報]</strong>の項目にいき、<strong>[仮売上]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/booking/share/6.jpg" /></div>
<p>クリックすると、再度確認のメッセージがでてきますので、<strong>[OK]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/booking/share/8.jpg" /></div>
<p>支払受取を処理おこない、成功メッセージががでてきましたら、予約受付処理の完了となり、ステータスが[予約済み]に変更されます。<br/></p>
<div class="help-shot"><img src="/images/help/booking/share/10.jpg" /></div>
</div>
<h4><i class="fa fa-exclamation-circle" aria-hidden="true"></i>予約受付処理を行わず、予約の利用開始日が過ぎた場合</h4>
<div class="help_body_content">
<p>予約処理を行うことは、予約申し込みを確認し、承認したという確認となっております。<br/>
予約処理を行わなわず、利用開始日が過ぎた場合、予約は承認されなかったものとみなし、<strong>自動的にキャンセル</strong>されます。その場合、キャセル料はかかりません。</p>
</div>
