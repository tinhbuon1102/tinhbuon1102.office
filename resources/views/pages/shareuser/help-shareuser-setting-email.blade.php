<div class="help_header">
<h1>メールアドレス変更方法</h1>
</div>
<h4>1.メールアドレス欄の変更をクリック</h4>
<div class="help_body_content">
<p>設定ページにて、メールアドレス項目の付近にある<strong>[変更]</strong>リンクをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/02_share.jpg" /></div>
</div>
<h4>2.新しいメールアドレスを入力</h4>
<div class="help_body_content">
<p>クリック後、メールアドレスフォームが編集可能となりますので、変更する新しいメールアドレスを入力してください。<br/>
入力後、<strong>[メールアドレスを変更]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/03_share.jpg" /></div>
<p>変更をクリック後、変更完了のメッセージがでましたら、メールアドレスの変更が完了となります。</p>
<div class="help-shot"><img src="/images/help/setting/04_share.jpg" /></div>
</div>
<h4>3.メール認証を行う</h4>
<div class="help_body_content">
<p>メールアドレス変更後、変更メールアドレス宛にメール認証のためのメールが送信されます。メールをご確認頂き、<strong>[メールアドレス認証]</strong>ボタン、またはその下のurlをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/05_share.jpg" /></div>
<p>メール認証完了ページが表示されましたら、メール認証完了となります。</p>
<div class="help-shot"><img src="/images/help/setting/06_share.jpg" /></div>
<p class="warn">※メール認証を行わないと、以下の様にアラートが表示されます。<br/>認証メール届かない場合は、<strong>[メールアドレスを認証]</strong>ボタンをクリックすると、再度メールが送信されます。</p>
<div class="help-shot"><img src="/images/help/setting/07_share.jpg" /></div>
</div>