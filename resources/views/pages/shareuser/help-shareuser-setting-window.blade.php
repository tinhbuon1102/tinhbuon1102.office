<h4>窓口担当者追加</h4>
<div class="help_body_content">
<p>設定ページにて、<strong>[窓口担当者]</strong>にいき、<strong>[担当者追加]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/setting/16_share.jpg" /></div>
<p>クリックすると、新規担当者プロフィール情報を入力するフォームが表示されます。各項目を入力し、<strong>[保存]</strong>ボタンをクリックすると設定完了です。</p>
<div class="help-shot"><img src="/images/help/setting/17_share.jpg" /></div>
</div>