</div>
<h4>1.予約一覧ページを開く</h4>
<div class="help_body_content">
<p>メニューから<strong>[予約リスト]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/book/cb1.jpg"></div>
</div>
<h4>2.キャンセルをリクエスト</h4>
<div class="help_body_content">
<p>一番右にある<strong>[キャンセル]</strong>ボタンをクリックしてください。<br/>
キャンセル済み、不可な場合は、キャンセルボタンは表示されません。</p>
<div class="help-shot"><img src="/images/help/book/canb1.jpg"></div>
<p><a href="{{url('cancel-policy')}}" class="color-link" target="_blank"><strong>[キャンセルポリシー]</strong></a>に基づく<strong>キャンセル料金</strong>をご確認の上、<strong>[続ける]</strong>をクリックしてください。</p>
<p class="red"><i class="fa fa-exclamation-triangle" aria-hidden="true"></i>一度キャンセルした予約は取り消しができませんので、ご注意ください。</p>
