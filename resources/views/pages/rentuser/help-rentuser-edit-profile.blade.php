<h1>基本プロフィール設定</h1>
</div>
<h4>1.プロフィール編集ページを開く</h4>
<div class="help_body_content">
<p>ログイン後、ダッシュボードの右側にある<strong>[プロフィールアイコン]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/profile/1.jpg" /></div>
</div>
<h4>2.基本プロフィールを編集する</h4>
<div class="help_body_content">
<p>右側にある<strong>[プロフィール編集]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/profile/2.jpg" /></div>
<p>以下の様にペンマークのアイコン<strong>[<i class="fa fa-pencil awesome-icon mgn-eq"></i>]</strong>、<strong>[写真変更リンク]</strong>や<strong>[実績追加]</strong>ボタンが表示されます。</p>
<ol class="list-type-alp">
<li><strong>紹介文</strong><br/>あなたの自己紹介文を書きましょう。（→<a href="#caution01" class="colorboxInline cboxElement"><i class="fa fa-info-circle" aria-hidden="true"></i>詳細</a>）
<div class="modals">
            <div id="caution01">
                <div class="modalContents">
                    <h3 class="headingTypeC01"><span>紹介文の書き方</span></h3>
                    <p class="alignCenter"><img src="/images/help/profile/4.jpg" /></p>
                    <ol class="numberListTypeA01">
                        <li>自己紹介文を入力してください。</li>
                        <li>保存をクリックしてください。</li>
                    </ol>
                    <!-- /#modalContents --></div>
                <!-- /#caution01 --></div>
                </div><!--/modals-->
</li>
<li><strong>スキル</strong><br/>あなたのスキルを選択しましょう。(複数選択可)（→<a href="#caution02" class="colorboxInline cboxElement"><i class="fa fa-info-circle" aria-hidden="true"></i>詳細</a>）
<div class="modals">
            <div id="caution02">
                <div class="modalContents">
                    <h3 class="headingTypeC01"><span>スキルの設定方法</span></h3>
                    <p class="alignCenter"><img src="/images/help/profile/5.jpg" /></p>
                    <ol class="numberListTypeA01">
                        <li>入力フォームをクリックし、スキルを選択してください。</li>
                        <li>保存をクリックしてください。</li>
                    </ol>
                    <!-- /#modalContents --></div>
                <!-- /#caution01 --></div>
                </div><!--/modals-->
</li>
<li><strong>プロフィール写真</strong><br/>プロフィール写真を追加しましょう。（→<a href="#caution03" class="colorboxInline cboxElement"><i class="fa fa-info-circle" aria-hidden="true"></i>詳細</a>）
<div class="modals">
            <div id="caution03">
                <div class="modalContents">
                    <h3 class="headingTypeC01"><span>プロフィール写真追加方法</span></h3>
