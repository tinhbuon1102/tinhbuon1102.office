<div class="help_header">
<h1>予約のキャンセル方法</h1>
</div>
<h4>1.予約一覧ページを開く</h4>
<div class="help_body_content">
<p>ログイン後、メニューから<strong>[予約リスト]</strong>をクリック、または左メニューから<strong>[予約]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/booking/share/1.jpg" /></div>
</div>
<h4>2.キャンセルしたい予約詳細を確認する</h4>
<div class="help_body_content">
<p>キャンセル処理を行いたい予約の<strong>[詳細]</strong>ボタンをクリックし、予約申し込み内容をを確認してください。</p>
<div class="help-shot"><img src="/images/help/booking/share/5.jpg" /></div>
</div>
<h4>3.キャンセル処理を行う</h4>
<div class="help_body_content">
<p>※予約ステータスが<strong>[利用中]</strong>、または<strong>[完了]</strong>の場合は、利用が開始している、または終了しているためキャンセルが不可能です。</p>
<div class="mgb-30">
<h5 id="acMenu">a.予約ステータスが<span class="btn btn-pending-alt btn-h5">処理中</span>の予約キャンセル方法</h5>
<div class="ac-content">
<p>処理中の予約を受け付けず、キャンセルする場合は、以下のどちらかの操作方法で予約の受取拒否をし、キャンセル処理を行って下さい。</p>
<p>A.<strong>画面右上の、赤ボタンの<strong>[予約受付拒否]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/booking/share/7-1.jpg" /></div>
<p>B.<strong>[支払情報]</strong>の項目にいき、[仮売上]の右側にある<strong>[<span class="ns_blue-arrow ns_down caret"></span>]</strong>をクリックしてください。<br/>
クリックすると<strong>[受取拒否]</strong>というオプションがでてきますので、それをクリックして下さい。</p>
<div class="help-shot"><img src="/images/help/booking/share/7.jpg" /></div>
<p>クリックすると、再度確認のメッセージがでてきます。、<br/><strong>キャンセル料</strong>についての確認が表示されますので、よく確認してから<strong>[続ける]</strong>ボタンをクリックしてください。</p>
<p><i class="fa fa-info-circle" aria-hidden="true"></i>キャンセル料については<a href="{{url('cancel-policy')}}" class="color-link" target="_blank"><strong>[キャンセルポリシー]</strong></a>をご参照ください。</p>
<p class="red"><i class="fa fa-exclamation-triangle" aria-hidden="true"></i>一度キャンセルした予約は取り消しができませんので、ご注意ください。</p>
<div class="help-shot"><img src="/images/help/booking/share/9.jpg" /></div>
<p>支払いの受取拒否おこない、成功メッセージががでてきましたら、予約受付キャンセルの完了となり、ステータスが[キャンセル]に変更されます。<br/>
合計金額の欄に返金率と返金額が表示され、支払状況が[返金済み]に変更されます。</p>
<div class="help-shot"><img src="/images/help/booking/share/11.jpg" /></div>
</div>
</div>
<div class="mgb-30">
<h5 id="acMenu">b.予約ステータスが<span class="btn btn-reserved-alt btn-h5">予約済み</span>の予約キャンセル方法</h5>
<div class="ac-content">
<p>予約済みの予約をキャンセルする場合は、以下の操作方法で返金処理をし、キャンセル処理を行って下さい。</p>
<p><strong>[支払情報]</strong>の項目にいき、[本売上]の右側にある<strong>[<span class="ns_blue-arrow ns_down caret"></span>]</strong>をクリックしてください。<br/>
クリックすると<strong>[返金処理]</strong>というオプションがでてきますので、それをクリックして下さい。</p>
<div class="help-shot"><img src="/images/help/booking/share/12.jpg" /></div>
<p>クリックすると、再度確認のメッセージがでてきます。、<br/><strong>キャンセル料</strong>についての確認が表示されますので、よく確認してから<strong>[続ける]</strong>ボタンをクリックしてください。</p>
<p><i class="fa fa-info-circle" aria-hidden="true"></i>キャンセル料については<a href="{{url('cancel-policy')}}" class="color-link" target="_blank"><strong>[キャンセルポリシー]</strong></a>をご参照ください。</p>
<p class="red"><i class="fa fa-exclamation-triangle" aria-hidden="true"></i>一度キャンセルした予約は取り消しができませんので、ご注意ください。</p>
<div class="help-shot"><img src="/images/help/booking/share/9.jpg" /></div>
<p>支払いの返金処理おこない、成功メッセージががでてきましたら、予約受付キャンセルの完了となり、ステータスが[キャンセル]に変更されます。<br/>
合計金額の欄に返金率と返金額が表示され、支払状況が[返金済み]に変更されます。</p>
<div class="help-shot"><img src="/images/help/booking/share/11.jpg" /></div>
</div>
