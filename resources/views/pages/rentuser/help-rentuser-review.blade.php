<h1>レビュー投稿方法</h1>
</div>
<h4>1.レビューページを開く</h4>
<div class="help_body_content">
<p>メニューから<strong>[レビュー]</strong>をクリックしてください。</p>
<div class="help-shot"><img src="/images/help/review/rent/1.jpg" /></div>
</div>
<h4>2.レビュー投稿をする</h4>
<div class="help_body_content">
<p>完了した予約のリストが表示されますので、レビューしたい予約を確認し、<strong>[レビューを書く]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/review/rent/2.jpg" /></div>
<p>以下のレビュー項目を選択、記入し、<strong>[レビューを投稿]</strong>ボタンをクリックしてください。</p>
<ol class="list-type-alp">
<li><strong>スペース評価</strong><br/>1-5のレートで評価してください。
<ol class="ol-list">
<li><strong>清潔さ</strong><br/>スペースが清潔だったかの評価</li>
<li><strong>親切度</strong><br/>対応などのが良かったかの評価</li>
<li><strong>オフィス環境</strong><br/>オフィス環境として良かったかの評価</li>
<li><strong>金額設定</strong><br/>金額設定の評価</li>
<li><strong>再利用率</strong><br/>再度利用したいかの評価</li>
</ol>
</li>
<li><strong>コメント</strong><br/>スペースに対しての評価コメントを書いてください。</li>
</ol>
