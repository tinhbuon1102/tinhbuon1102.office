<div class="help_body_content">
<p>スペースの内装や外観など、メイン画像とギャラリー画像をアップロードしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/3.jpg" /></div>
</div>

<h4>4.利用金額と期間を設定</h4>
<div class="help_body_content">
<p>スペース利用金額と最低利用時間を設定してください。<br/>
時間毎、日毎、週毎、月毎の利用タイプにより、最低利用時間・期間のオプション選択が変わります。</p>
<p>平日、土曜、日曜、祝日で利用金額が変動する場合は、<strong>[平日と休日で金額を設定]</strong>のチェックボックスにチェックしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/4.jpg" /></div>
<p>チェックをいれますと、平日・土曜・日曜・祝日のそれぞれの利用金額の設定ができるフォームが表示されますので、そちらに金額を入力してください。</p>
<div class="help-shot"><img src="/images/help/list-space/5.jpg" /></div>
</div>

<h4>5.最終予約受付を設定</h4>
<div class="help_body_content">
<strong>最終予約受付期間について</strong>
<p>利用開始日前に◯時間前、◯日、◯週間、◯ヶ月前に最短で予約できるかの設定となります。<br/>
例)1時間前と設定した場合、利用開始時間が14時であれば、同日の13時までの予約受付完了にて予約ができる。<br/><br/>
※予約確認できる余裕をもった時間数や日数にて設定して下さい</p>
<div class="help-shot"><img src="/images/help/list-space/6.jpg" /></div>
</div>

<h4>6.基本利用可能時間帯を設定</h4>
<div class="help_body_content">
<p>曜日ごとに目安となる利用可能時間帯を設定して下さい。<br/>
※日毎での利用スペース提供の場合は、ここでの設定はデフォルトとして保存され、カレンダー設定にて日ごとに時間帯変更も可能です。</p>
<ol class="list-type-alp">
<li><strong>時間帯</strong><br/>9:00-18:00など、スペース利用可能時間帯を設定してください。</li>
<li><strong>終日利用不可</strong><br/>定休日や休日などで終日利用不可な場合は、こちらをチェックしてください。</li>
<li><strong>24時間利用可能</strong><br/>24時間利用可能な場合は、こちらをチェックしてください。</li>
</ol>
<div class="help-shot"><img src="/images/help/list-space/7.jpg" /></div>
</div>

<h4>7.スペースに含まれる設備を設定</h4>
<div class="help_body_content">
<p>デスク、イスなどの家具の数、wi-fiの有無など、スペース利用に含まれる設備の情報をご入力ください。</p>
<div class="help-shot"><img src="/images/help/list-space/8.jpg" /></div>
</div>

<h4>8.保存してスペース登録完了</h4>
<div class="help_body_content">
<p>全ての情報が入力し終えましたら、右上の<strong>[保存]</strong>ボタンをクリックし、登録が完了となります。</p>
</div>
