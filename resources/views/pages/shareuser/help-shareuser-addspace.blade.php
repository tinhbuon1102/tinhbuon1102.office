<h1>スペース追加方法</h1>
</div>
<h4>1.スペースページを開く</h4>
<div class="help_body_content">
<p>ログイン後、ダッシュボードページから<strong>[提供中スペース] &gt; [スペースを追加]</strong>、または<strong>[スペース登録]</strong>アラート、またはあなたへのお知らせ欄に表示されている<strong>[スペースを追加]</strong>ボタンをクリックしてください。</p>
<div class="help-shot"><img src="/images/help/list-space/1.jpg" /></div>
</div>
<a id="spaceinfo"></a>
<h4>2.スペース情報を入力</h4>
<div class="help_body_content">
<h5>基本情報</h5>
<p>スペース情報詳細となる基本情報を入力してください。</p>
<ol class="list-space-field">
<li><strong>タイトル</strong><br/>スペースのタイトルを入力してください。</li>
<li><strong>住所</strong><br/>スペースがある住所を入力してください。郵便番号を入力すると市区町村までの住所が自動入力されます。会社情報と同じ住所を使用する場合は会社情報と同じ住所を使用する<strong>[会社情報と同じ住所を使用する]</strong>にチェックをいれてください。</li>
<li><strong>スペースがある階</strong><br/>地上か地下のどちらかを選び、スペースがある階数を数字にてご入力ください。</li>
<li><strong>スペースタイプと利用可能人数</strong><br/>スペース利用方法のタイプ、スペースのタイプ、利用人数を選択、入力してください。
<p class="mgt-20"><strong>[スペース利用方法のタイプ]</strong></p>
<ul class="use-type">
<li><strong>時間毎</strong>&nbsp;-1時間毎で利用可能</li>
<li><strong>日毎</strong>&nbsp;-1日毎で利用可能</li>
<li><strong>週毎</strong>&nbsp;-1週間毎で利用可能</li>
<li><strong>月毎</strong>&nbsp;-1ヶ月毎で利用可能</li>
</ul>
<p class="mgt-20"><strong>[スペースのタイプ]</strong></p>
<ul class="use-type space-type-list">
<li><span class="icon-left"><i class="icon-space-icon-st-coworking icon-size-3"></i></span><span class="disc-right"><strong>コワーキング</strong><br/>会議室、打ち合わせスペースなどを他の人と共有し、オープンエリアにある共有スペース</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-open_desk icon-size-3"></i></span><span class="disc-right"><strong>オープンデスク</strong><br/>共有スペースの中でデスクなどが決められていないワークスペース</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-dedicated_desk icon-size-3"></i></span><span class="disc-right"><strong>専用デスク</strong><br/>オープンエリアにある専用デスクスペース(1人用)</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-meetingroom icon-size-3"></i></span><span class="disc-right"><strong>会議室</strong><br/>会議室用のスペース</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-seminar icon-size-3"></i></span><span class="disc-right"><strong>セミナースペース</strong><br/>セミナー用のスペース</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-private_office icon-size-3"></i></span><span class="disc-right"><strong>プライベートオフィス</strong><br/>個人用の個室のオフィス(1人用)</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-team_office icon-size-3"></i></span><span class="disc-right"><strong>チームオフィス</strong><br/>複数人用の個室オフィス</span></li>
<li><span class="icon-left"><i class="icon-space-icon-st-office icon-size-3"></i></span><span class="disc-right"><strong>オフィス</strong><br/>完全に独立した部屋全体を利用できるオフィス</span></li>
</ul>
<p class="mgt-20"><strong>[利用人数]</strong></p>
<p>スペースの利用可能人数を入力してください。<br/>
例)6人収容可能な会議室の場合、"6"と入力する。</p>
<p>
<span class="red"><i class="fa fa-exclamation-circle" aria-hidden="true"></i><strong>コワーキング</strong>、または<strong>オープンデスク</strong>を選択した場合は、<strong>予約(受付)可能人数</strong>を入力してください。</span><br/>
例)コワーキング、もしくはオープンデスクのスペースが4人分のシェア利用可能な場合、"4"と入力する。その場合、同じ時間帯、期間に4人分の予約受付が可能となります。<br/><br/>
※<strong>専用デスク</strong>、<strong>プライベートオフィス</strong>は1人用スペースなので、入力は必要でありません。</p>
</li>
<li><strong>スペース説明文</strong><br/>スペースの紹介文を入力してください。</li>
<li><strong>喫煙</strong><br/>スペース内にて禁煙、喫煙の可否を選択してください。</li>
<li><strong>飲食</strong><br/>スペース内にての飲食の可否を選択してください。</li>
<li><strong>特徴を追加(任意)</strong><br/>特徴やポイントがあれば、自由に入力してください。[+]ボタンで複数追加できます。</li>
</ol>
